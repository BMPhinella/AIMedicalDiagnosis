                        <p>The aims are to improve symptoms of hyperglycaemia and
                            minimise the risks of long-term microvascular and macrovascular
                            complications.</p>
                        <p>In patients with suspected type 1 diabetes, urgent treatment with
                            insulin is required and prompt referral to a specialist is usually
                            needed</p>
